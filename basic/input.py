# input que serve para receber dados do usuário
# input sempre retorna uma string

# para converter para outro tipo, usar as funções de conversão
# int() para inteiro
# float() para ponto flutuante
# bool() para booleano
# str() para string

# Exemplo 1

name = input("What is your name? ")
yearLaunch = int(input("What year did you launch your company? "))
gamePrice = float(input("How much does your game cost? "))
print(name)
print(yearLaunch)
print(gamePrice)

